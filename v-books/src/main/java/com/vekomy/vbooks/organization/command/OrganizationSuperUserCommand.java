package com.vekomy.vbooks.organization.command;

public class OrganizationSuperUserCommand {
	
	/**
	 * String variable holds action
	 */
	private String action;
	/**
	 * String variable holds fullName
	 */
	private String fullName;
		
		/**
		 * String variable holds superUsername
		 */
		private String superUsername;
		/**
		 * String variable holds password
		 */
		private String password;
		/**
		 * String variable holds email
		 */
		private String email;
		/**
		 * int variable holds mobile
		 */
		private String mobile;
		/**
		 * int variable holds alternateMobile
		 */
		private String alternateMobile;
		/**
		 * @return the password
		 */
		public String getPassword() {
			return password;
		}
		/**
		 * @param password the password to set
		 */
		public void setPassword(String password) {
			this.password = password;
		}
		/**
		 * @return the email
		 */
		public String getEmail() {
			return email;
		}
		/**
		 * @param email the email to set
		 */
		public void setEmail(String email) {
			this.email = email;
		}
		/**
		 * @return the mobile
		 */
		public String getMobile() {
			return mobile;
		}
		/**
		 * @param mobile the mobile to set
		 */
		public void setMobile(String mobile) {
			this.mobile = mobile;
		}
		/**
		 * @return the alternateMobile
		 */
		public String getAlternateMobile() {
			return alternateMobile;
		}
		/**
		 * @param alternateMobile the alternateMobile to set
		 */
		public void setAlternateMobile(String alternateMobile) {
			this.alternateMobile = alternateMobile;
		}
		/**
		 * @return the action
		 */
		public String getAction() {
			return action;
		}
		/**
		 * @param action the action to set
		 */
		public void setAction(String action) {
			this.action = action;
		}
		/**
		 * @return the fullName
		 */
		public String getFullName() {
			return fullName;
		}
		/**
		 * @param fullName the fullName to set
		 */
		public void setFullName(String fullName) {
			this.fullName = fullName;
		}
		/**
		 * @return the superUsername
		 */
		public String getSuperUsername() {
			return superUsername;
		}
		/**
		 * @param superUsername the superUsername to set
		 */
		public void setSuperUsername(String superUsername) {
			this.superUsername = superUsername;
		}

}
