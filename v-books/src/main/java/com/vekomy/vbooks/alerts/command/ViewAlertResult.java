package com.vekomy.vbooks.alerts.command;

/**
 * @author swarupa
 *
 */
public class ViewAlertResult {
	
	/**
	 * Integer variable hold id.
	 */
	private Integer id;
	/**
	 * String variable holds alertCategory
	 */
	private String alertCategory;
	/**
	 * String variable holds alertType
	 */
	private String alertType;
	/**
	 * String variable holds alertName
	 */
	private String alertName;
	/**
	 * String variable holds alertDescription
	 */
	private String alertDescription;
	/**
	 * Integer variable holds alertCount
	 */
	private Integer alertCount;
	/**
	 * String variable holds alertEnableDisable
	 */
	private Boolean alertEnableDisable;
	/**
	 * String variable holds alertCriteria
	 */
	private String alertCriteria;
	
	
	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	/**
	 * @return the alertCategory
	 */
	public String getAlertCategory() {
		return alertCategory;
	}
	/**
	 * @param alertCategory the alertCategory to set
	 */
	public void setAlertCategory(String alertCategory) {
		this.alertCategory = alertCategory;
	}
	/**
	 * @return the alertType
	 */
	public String getAlertType() {
		return alertType;
	}
	/**
	 * @param alertType the alertType to set
	 */
	public void setAlertType(String alertType) {
		this.alertType = alertType;
	}
	/**
	 * @return the alertName
	 */
	public String getAlertName() {
		return alertName;
	}
	/**
	 * @param alertName the alertName to set
	 */
	public void setAlertName(String alertName) {
		this.alertName = alertName;
	}
	/**
	 * @return the alertDescription
	 */
	public String getAlertDescription() {
		return alertDescription;
	}
	/**
	 * @param alertDescription the alertDescription to set
	 */
	public void setAlertDescription(String alertDescription) {
		this.alertDescription = alertDescription;
	}
	/**
	 * @return the alertCount
	 */
	public Integer getAlertCount() {
		return alertCount;
	}
	/**
	 * @param alertCount the alertCount to set
	 */
	public void setAlertCount(Integer alertCount) {
		this.alertCount = alertCount;
	}
	/**
	 * @return the alertEnableDisable
	 */
	public Boolean getAlertEnableDisable() {
		return alertEnableDisable;
	}
	/**
	 * @param alertEnableDisable the alertEnableDisable to set
	 */
	public void setAlertEnableDisable(Boolean alertEnableDisable) {
		this.alertEnableDisable = alertEnableDisable;
	}
	/**
	 * @return the alertCriteria
	 */
	public String getAlertCriteria() {
		return alertCriteria;
	}
	/**
	 * @param alertCriteria the alertCriteria to set
	 */
	public void setAlertCriteria(String alertCriteria) {
		this.alertCriteria = alertCriteria;
	}
	
	

}
